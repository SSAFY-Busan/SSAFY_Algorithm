# 소인수분해

# 정수 N이 주어졌을 때, 소인수분해하는 프로그램을 작성하시오.

# 첫째 줄에 정수 N (1 ≤ N ≤ 10,000,000)이 주어진다.

N = int(input())    # 입력
s = 2               # 처음은 2
while True:         # 반복
    if N == 1:      # 1되면 멈춰
        break
    if N % s == 0:  # 나눠서 0이 남으면 
        N = N//s    # 나눠서 저장하고
        print(s)    # s 는 출력하고
    else:           # 안나눠지면
        s = s + 1   # s 를 1 증가